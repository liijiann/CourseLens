function buildShadow(pixels: Pixel[]): string {
  return pixels
    .map(([col, row, color]) => `${col * 4}px ${row * 4}px 0 1.5px ${color}`)
    .join(', ');
}

// Pre-compute all shadows
const ALL_SHADOWS = ALL_CATS.map(buildShadow);

function getCatSize(pixels: Pixel[]): { width: number; height: number } {
  let maxCol = 0, maxRow = 0;
  for (const [col, row] of pixels) {
    if (col > maxCol) maxCol = col;
    if (row > maxRow) maxRow = row;
  }
  return { width: (maxCol + 1) * 4, height: (maxRow + 1) * 4 };
}

const ALL_SIZES = ALL_CATS.map(getCatSize);

export default function PixelCat({ className = '' }: { className?: string }) {
  const [index, setIndex] = useState<number | null>(null);

  useEffect(() => {
    setIndex(Math.floor(Math.random() * ALL_CATS.length));
  }, []);

  if (index === null) {
    return <div className={className} style={{ width: 52, height: 48 }} />;
  }

  const size = ALL_SIZES[index];
  const shadow = ALL_SHADOWS[index];
  // Offset to handle negative columns
  const pixels = ALL_CATS[index];
  let minCol = 0;
  for (const [col] of pixels) { if (col < minCol) minCol = col; }
  const offsetX = -minCol * 4;

  return (
    <div
      className={className}
      style={{
        width: size.width + 3,
        height: size.height + 3,
        position: 'relative',
      }}
    >
      <div
        style={{
          position: 'absolute',
          top: 1.5,
          left: offsetX + 1.5,
          width: 4,
          height: 4,
          boxShadow: shadow,
          imageRendering: 'pixelated',
        }}
      />
    </div>
  );
}
